/// AutoCAD Color Index (ACI) conversion utilities.
/// Mirrors libdxfrw `DRW::dxfColors` table and group code conventions.
public enum DXFColorTable {

    /// Convert ACI (1-255) + optional 24-bit color override to RGBA.
    /// color24 >= 0 takes priority (DXF group 420).
    public static func aciToRGBA(_ aci: Int32, color24: Int32) -> ColorRGBA {
        if color24 >= 0 {
            let r = UInt8((color24 >> 16) & 0xFF)
            let g = UInt8((color24 >> 8) & 0xFF)
            let b = UInt8(color24 & 0xFF)
            return ColorRGBA(r: r, g: g, b: b)
        }
        let idx = abs(Int(aci))
        guard idx >= 1, idx < 256 else { return .white }
        let rgb = dxfColors[idx]
        return ColorRGBA(r: rgb[0], g: rgb[1], b: rgb[2])
    }

    /// Convert RGBA to ACI (best match). Returns 7 (white) by default.
    public static func rgbaToACI(_ color: ColorRGBA) -> Int32 {
        if color.a == 0 { return 256 } // BYLAYER
        // Check exact match in palette
        var best = 7
        var bestDist = Double.infinity
        for i in 1..<256 {
            let c = dxfColors[i]
            let dr = Double(Int(color.r) - Int(c[0]))
            let dg = Double(Int(color.g) - Int(c[1]))
            let db = Double(Int(color.b) - Int(c[2]))
            let dist = dr * dr + dg * dg + db * db
            if dist < bestDist {
                bestDist = dist
                best = i
            }
        }
        return Int32(best)
    }

    /// Convert RGBA to 24-bit DXF true color (group 420). Returns nil if not set.
    public static func rgbaToTrueColor(_ color: ColorRGBA) -> Int32? {
        if color.a == 0 { return nil }
        return (Int32(color.r) << 16) | (Int32(color.g) << 8) | Int32(color.b)
    }

    /// DXF transparency (group 440) → opacity 0..1
    public static func transparencyToOpacity(_ trans: Int32) -> Double {
        if trans < 0 { return 1.0 }
        if trans == 0 { return 1.0 }
        if trans <= 90 { return 1.0 - Double(trans) / 100.0 }
        return Double(trans) / 255.0
    }

    /// DXF line weight → mm
    public static func lineWeightToMM(_ lw: Double) -> Double {
        if lw < 0 { return 0.25 }
        return lw / 100.0
    }

